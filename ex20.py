a = float(input("enter la valeur de a :"))
b = float(input("enter la valeur de b :"))
op = (input("enter l operation + - / * :"))
if  op == "+":
    print("a + b =",a + b)
elif  op == "-":
    print("a - b =",a - b)
elif  op == "*":
    print("a * b =",a * b)
elif  op == "/":
    print("a / b =",a / b)